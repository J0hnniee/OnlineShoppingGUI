    private boolean isEmpty() {
        String email = emailField.getText().trim();
        String username = usernameField.getText().trim();
        String password = String.valueOf(passwordField.getPassword()).toString();
        String firstName = firstNameField.getText().trim();
        String lastName = lastNameField.getText().trim();
        String address = addressField.getText().trim();

        if (firstName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "First name can not be empty", "Warning", JOptionPane.WARNING_MESSAGE);
            return true;
        }
        if (lastName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Last name can not be empty", "Warning", JOptionPane.WARNING_MESSAGE);
            return true;
        }
        if (email.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Email can not be empty", "Warning", JOptionPane.WARNING_MESSAGE);
            return true;
        }
        if (username.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Username can not be empty", "Warning", JOptionPane.WARNING_MESSAGE);
            return true;
        }
        if (password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Password can not be empty", "Warning", JOptionPane.WARNING_MESSAGE);
            return true;
        }

        if (address.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Address can not be empty", "Warning", JOptionPane.WARNING_MESSAGE);
            return true;
        }
        return false;
    }

